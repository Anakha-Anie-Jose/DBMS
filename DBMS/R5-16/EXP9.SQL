
SET SERVEROUTPUT ON;
CREATE OR REPLACE PROCEDURE minnum(X IN NUMBER,Y IN NUMBER,Z OUT NUMBER)
as
BEGIN
  IF X<Y THEN
     Z:=X;
  ELSE
     Z:=Y;
  END IF;
END minnum;
DECLARE
X NUMBER;
Y NUMBER;
Z NUMBER;
BEGIN
X:=10;
Y:=20;
minnum(X,Y,Z);
DBMS_OUTPUT.PUT_LINE('MINIMUM IS '||Z);
END;
/

