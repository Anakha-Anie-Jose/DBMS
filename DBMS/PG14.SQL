SET SERVEROUTPUT ON;
DECLARE
NUM INT;
RES INT;
FUNCTION FACT(NUM IN INT)
RETURN INT AS
F INT;
I NUMBER;
BEGIN
F:=1;
FOR I IN 1..NUM LOOP
  F:=F*I;
END LOOP;
RETURN F;
END;
BEGIN
NUM:=&NUM;
RES:=FACT(NUM);
DBMS_OUTPUT.PUT_LINE('FACTORIAL OF '||NUM||' IS '||RES);
END;
/
